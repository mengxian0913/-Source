void scrollRight() {
	unsigned char tmp[10];
	int i = 0, j = 0;
	for(i = 0, j = 127; i < 8; i++) {
		tmp[i] = map[j];
		j += 128;
	}
	
	for(i = 127; i >= 1; i--) {
		for(j = 0; j < 8; j++) {
			map[128 * j + i] = map[128 * j + i - 1];
		}
	}
	
	for(i = 0; i < 8; i++) {
		map[128 * i] = tmp[i];
	}

	return;
}

void scrollLeft() {
	
	unsigned char tmp[10];
	int i = 0, j = 0;
	for(i = 0; i < 8; i++) {
		tmp[i] = map[128 * i];
	}
	
	for(i = 0; i < 128 - 1; i++) {
		for(j = 0; j < 8; j++) {
			map[128 * j + i] = map[128 * j + i + 1];
		}
	}
	
	for(i = 0; i < 8; i++) {
		map[128 * i + 127] = tmp[i];
	}

	return;
}



